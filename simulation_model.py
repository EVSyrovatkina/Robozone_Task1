import simpy
import random

# Базовые физические константы Ozon Tech (Раздел 1)
TOTAL_SIM_TIME = 86400  # Сутки в секундах
INCOMING_RATE_PER_SEC = 1.0278  # 3700 КТЯ в час / 3600 с
SHIPPING_RATE_PER_SEC = 1.0667  # Проводимость 24 доков
BUFFER_CAPACITY = 1850  # Емкость динамического буфера

class SortingCenter:
    def __init__(self, env):
        self.env = env
        self.buffer = simpy.Container(env, capacity=BUFFER_CAPACITY, init=0)
        self.total_processed = 0
        self.peak_buffer_length = 0

    def incoming_flow(self):
        while True:
            # Случайный наплыв плотности по закону Пуассона
            yield self.env.timeout(random.expovariate(INCOMING_RATE_PER_SEC))

            # Если доки заблокированы или перегружены, коробки идут в буфер
            current_gate_busy = random.random() > 0.9635 # Коэффициент утилизации
            if current_gate_busy or self.buffer.level > 0:
                if self.buffer.level < BUFFER_CAPACITY:
                    yield self.buffer.put(1)
                    if self.buffer.level > self.peak_buffer_length:
                        self.peak_buffer_length = self.buffer.level
                else:
                    print(f"[{self.env.now:.1f}с] КРИТИЧЕСКОЕ ПЕРЕПОЛНЕНИЕ БУФЕРА!")
            else:
                self.total_processed += 1

    def shipping_flow(self):
        while True:
            # Имитация хаотичных сбоев и пересменки фур на доках
            yield self.env.timeout(random.expovariate(SHIPPING_RATE_PER_SEC))
            if self.buffer.level > 0:
                yield self.buffer.get(1)
                self.total_processed += 1

# Запуск суточного стресс-тестирования
random.seed(42) # Фиксация для воспроизводимости результатов
env = simpy.Environment()
sc = SortingCenter(env)

env.process(sc.incoming_flow())
env.process(sc.shipping_flow())
env.run(until=TOTAL_SIM_TIME)

